package com.sgi.util;

public class MapperEntry {
	public int FOR_FACULTY;
	public String COURSE;
	public String BRANCH;
	public String YEAR;
	public String SECTION;

	public MapperEntry(int for_faculty,String course, String branch, String year, String section) {
		FOR_FACULTY = for_faculty;
		COURSE = course;
		BRANCH = branch;
		SECTION = section;
		YEAR = year;
	}
}
